package com.github.yingzhuo.carnival.restful.security.token;

import lombok.val;

import java.io.Serializable;

/**
 * @author 应卓
 */
public class UsernamePasswordToken implements Token, Serializable {

    private static final long serialVersionUID = 3217442894344389575L;

    private String username;
    private String password;

    public UsernamePasswordToken() {
        super();
    }

    public UsernamePasswordToken(String username, String password) {
        this.username = username;
        this.password = password;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        val that = (UsernamePasswordToken) o;
        return username.equals(that.username) && password.equals(that.password);
    }

    @Override
    public int hashCode() {
        int result = username.hashCode();
        result = 31 * result + password.hashCode();
        return result;
    }

}
