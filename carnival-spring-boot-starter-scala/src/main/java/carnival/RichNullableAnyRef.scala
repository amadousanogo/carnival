package carnival

/**
  * @author 应卓
  */
private[carnival] class RichNullableAnyRef[T](o: T) {

  // -----------------------------------------------------------------------------------------------------------------

  def option: Option[T] = Option(o)


  // -----------------------------------------------------------------------------------------------------------------

  def ?!(t: => T): T = o match {
    case null => t
    case _ => o
  }

  def defaultIfNull(t: => T): T = o ?! t

}
