package com.github.yingzhuo.carnival.mvc.download;

import org.springframework.core.MethodParameter;
import org.springframework.web.context.request.NativeWebRequest;
import org.springframework.web.method.support.HandlerMethodReturnValueHandler;
import org.springframework.web.method.support.ModelAndViewContainer;

public class DownloadHandlerMethodReturnValueHandler implements HandlerMethodReturnValueHandler {

    @Override
    public boolean supportsReturnType(MethodParameter returnType) {
        return DownloadEntity.class.isAssignableFrom(returnType.getParameterType());
    }

    @Override
    public void handleReturnValue(Object returnValue, MethodParameter returnType, ModelAndViewContainer mavContainer, NativeWebRequest webRequest) throws Exception {
        DownloadEntity entity = (DownloadEntity) returnValue;
        mavContainer.setView(new DownloadView(entity.getFilename(), entity.getFilenameEncoding(), entity.getHttpStatus(), entity.getInputStream()));
    }

}
